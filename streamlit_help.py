def render_mathematical_formulas_guide():
    """Render mathematical formulas guide."""
    st.subheader("🧮 Mathematical Formulas")
    
    st.write("""
    ### Core Financial Formulas
    
    **Free Cash Flow:**
    ```
    FCF = Operating Cash Flow - Capital Expenditures
    FCFE = FCF - Net Debt Issued
    FCFF = FCF + Interest × (1 - Tax Rate)
    ```
    
    **DCF Valuation:**
    ```
    PV = FCF₁/(1+r)¹ + FCF₂/(1+r)² + ... + Terminal Value/(1+r)ⁿ
    Terminal Value = FCFₙ × (1+g) / (r-g)
    ```
    
    **DDM Valuation:**
    ```
    Gordon Growth: P₀ = D₁/(r-g)
    Multi-Stage: P₀ = Σ[Dₜ/(1+r)ᵗ] + Pₙ/(1+r)ⁿ
    ```
    
    **P/B Analysis:**
    ```
    P/B Ratio = Market Price / Book Value per Share
    Fair Value = Historical Average P/B × Current Book Value
    ```
    """)


