def render_fcf_analysis_guide():
    """Render FCF analysis guide."""
    st.subheader("📈 Free Cash Flow Analysis Guide")
    
    st.write("""
    ### Understanding Free Cash Flow
    
    **What is FCF?**
    Free Cash Flow represents the cash generated by operations after capital expenditures.
    It's a key indicator of financial health and growth sustainability.
    
    **FCF Types Supported:**
    - **FCFE (Free Cash Flow to Equity)**: Available to equity holders
    - **FCFF (Free Cash Flow to Firm)**: Available to all capital providers
    - **Levered FCF**: Cash flow after debt service
    
    ### Key Metrics Displayed:
    - Historical FCF trends (5+ years)
    - Growth rates (YoY, CAGR)
    - FCF margins and efficiency ratios
    - Capital allocation analysis
    
    ### Interpretation Tips:
    - ✅ **Positive, Growing FCF**: Strong financial position
    - ⚠️ **Declining FCF**: May indicate business challenges
    - 📊 **Volatile FCF**: Check for cyclical patterns or one-time items
    """)


def render_dcf_valuation_guide():
    """Render DCF valuation guide."""
    st.subheader("💰 DCF Valuation Guide")
    
    st.write("""
    ### Discounted Cash Flow Model
    
    **Purpose:**
    Estimate intrinsic company value by discounting future cash flows to present value.
    
    **Key Components:**
    1. **Historical FCF**: Base for projections
    2. **Growth Assumptions**: Revenue and FCF growth rates
    3. **Terminal Value**: Long-term value assumption
    4. **Discount Rate**: Risk-adjusted required return (WACC)
    
    ### Model Parameters:
    - **Projection Period**: Typically 5-10 years
    - **Terminal Growth Rate**: Long-term sustainable growth (2-4%)
    - **Discount Rate**: Company's weighted average cost of capital
    
    ### Output Interpretation:
    - **Enterprise Value**: Total company value
    - **Equity Value**: Value attributable to shareholders
    - **Price per Share**: Estimated fair value per share
    - **Sensitivity Analysis**: Value ranges based on assumption changes
    """)


